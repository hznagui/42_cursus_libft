char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*u;

	i = 0;
	u = (char *)s;
	while (s[i])
		i++;
	while (i)
	{
		if (s[i] == c)
			return (u + i);
		i--;
	}
	return (0);
}
